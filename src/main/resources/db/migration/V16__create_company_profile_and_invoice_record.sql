CREATE TABLE IF NOT EXISTS company_profile (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_email VARCHAR(255) NOT NULL,
    legal_name VARCHAR(255),
    trade_name VARCHAR(255),
    cnpj VARCHAR(32),
    tax_regime VARCHAR(120),
    activity VARCHAR(255),
    state_registration VARCHAR(120),
    municipal_registration VARCHAR(120),
    opening_date VARCHAR(32),
    business_phone VARCHAR(64),
    business_email VARCHAR(255),
    website VARCHAR(255),
    contact_name VARCHAR(255),
    zip_code VARCHAR(32),
    street VARCHAR(255),
    number VARCHAR(64),
    complement VARCHAR(255),
    district VARCHAR(255),
    city VARCHAR(255),
    state VARCHAR(32),
    invoice_email VARCHAR(255),
    pix_key VARCHAR(255),
    main_bank VARCHAR(255),
    billing_notes TEXT,
    accountant_name VARCHAR(255),
    accountant_email VARCHAR(255),
    accountant_phone VARCHAR(64),
    payroll_notes TEXT,
    operation_notes TEXT,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT uk_company_profile_user_email UNIQUE (user_email)
);

CREATE INDEX IF NOT EXISTS idx_company_profile_user_email ON company_profile(user_email);

CREATE TABLE IF NOT EXISTS invoice_record (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_email VARCHAR(255) NOT NULL,
    source_path VARCHAR(1024) NOT NULL,
    filename VARCHAR(255) NOT NULL,
    issue_year VARCHAR(8),
    month_folder VARCHAR(120),
    issue_date VARCHAR(64),
    invoice_number VARCHAR(64),
    customer_document VARCHAR(64),
    customer_name VARCHAR(255),
    customer_email VARCHAR(255),
    customer_city VARCHAR(255),
    gross_amount DECIMAL(19,2),
    iss_amount DECIMAL(19,2),
    net_amount DECIMAL(19,2),
    service_type VARCHAR(255),
    notes TEXT,
    canceled BOOLEAN NOT NULL DEFAULT FALSE,
    relative_path VARCHAR(1024),
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT uk_invoice_record_user_email_source_path UNIQUE (user_email, source_path)
);

CREATE INDEX IF NOT EXISTS idx_invoice_record_user_email ON invoice_record(user_email);
CREATE INDEX IF NOT EXISTS idx_invoice_record_user_email_issue_year ON invoice_record(user_email, issue_year);
